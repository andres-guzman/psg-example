# Ejemplo 1: isascii()
numero_1 = "100.000".isascii()
print(numero_1)

# Ejemplo 2: isdecimal()
numero_2 = "100.0".isdecimal()
print(numero_2)

# Ejemplo 3: islower()
texto_1 = "Texto de ejemplo".islower()
print(texto_1)

# Ejemplo 4: istitle()
texto_2 = "Texto De Ejemplo".istitle()
print(texto_2)

# Ejemplo 5: isuppe()
texto_3 = "TEXTO DE EJEMPLO".isupper()
print(texto_3)
