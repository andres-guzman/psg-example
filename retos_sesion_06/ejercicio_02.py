# Operador XNOR
def comparacion(a, b):
    return a == b

# Ejemplo
valor_a = 5
valor_b = 3

resultado = comparacion(valor_a, valor_b)
print("XNOR de", valor_a, "y", valor_b, "es:", resultado)

# Imprimir la comparación
print(valor_a == valor_b)
